package weightmanagement;

public class BodyData {

	private double height;

	private double weight;

	private double bmi;

	private double appropriateWeight;

	public double getHeight() {
		return height;
	}

	public void setHeight(double height) {
		this.height = height;
	}

	public double getWeight() {
		return weight;
	}

	public void setWeight(double weight) {
		this.weight = weight;
	}

	public double getBmi() {
		return bmi;
	}

	public void setBmi(double bmi) {
		this.bmi = bmi;
	}

	public double getAppropriateWeight() {
		return appropriateWeight;
	}

	public void setAppropriateWeight(double appropriateWeight) {
		this.appropriateWeight = appropriateWeight;
	}



}
